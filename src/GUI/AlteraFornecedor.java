    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Fornecedor M = new Fornecedor();
        M.setFornecedor_id(id);
        if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "O Campo nome não pode estar vazio");
        } else {

            M.setNome(jTextField2.getText());
            M.setEmail(jTextField3.getText());
            M.setTelefone(jTextField5.getText());
            M.setEndereco(jTextField6.getText());

            FornecedorDAO PDAO = new FornecedorDAO();
            PDAO.atualizar(M);
            JOptionPane.showMessageDialog(rootPane, "Atualização efetuada com sucesso!!");
            dispose();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

