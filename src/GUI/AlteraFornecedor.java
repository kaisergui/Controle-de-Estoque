    public static void carregaCombo() {
        jComboBox1.removeAllItems();
        FornecedorDAO MDAO = new FornecedorDAO();
        Fornecedores = MDAO.listar();
        for (int i = 0; i < Fornecedores.size(); i++) {
            jComboBox1.addItem(Fornecedores.get(i).getNome());
        }
    }

    public void carrega() {
        FornecedorDAO MDAO = new FornecedorDAO();
        M = MDAO.procurar(id);
        String fornecedor_id = String.valueOf(M.getFornecedor_id());
        jTextField4.setText(fornecedor_id);
        jTextField2.setText(M.getNome());
        jTextField3.setText(M.getEmail());
        jTextField5.setText(M.getTelefone());
        jTextField6.setText(M.getEndereco());
    }
