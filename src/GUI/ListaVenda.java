    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        String venda_id, produto_id, cliente_id, quantidade, data, hora, total;

        if (jTable1.getRowCount() > 0) {
            while (jTable1.getRowCount() > 0) {
                table.removeRow(0);
            }
        }

        VendaDAO PDAO;
        try {
            PDAO = new VendaDAO();
            lista = PDAO.listar();
        } catch (Exception ex) {
            System.out.println("Erro de conex√£o ao banco.");
        }

        for (int posicao = 0; posicao < lista.size(); posicao++) {

            venda_id = String.valueOf(lista.get(posicao).getVenda_id());

            produto_id = String.valueOf(lista.get(posicao).getProduto_id());
             cliente_id = String.valueOf(lista.get(posicao).getCliente_id());
            quantidade = String.valueOf(lista.get(posicao).getQuantidade());
            total = String.valueOf(lista.get(posicao).getTotal());
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            data = (sdf.format(lista.get(posicao).getData_venda()));
            SimpleDateFormat format = new SimpleDateFormat("HH:mm:ss");
            hora = (format.format(lista.get(posicao).getHorario()));
            table.addRow(new String[]{venda_id, produto_id, cliente_id, quantidade, data, hora, total});
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_formWindowActivated

