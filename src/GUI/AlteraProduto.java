 public static void carregaCombo() throws ErpDAOException {
        jComboBox1.removeAllItems();
        ProdutoDAO MDAO = new ProdutoDAO();
        Produtos = MDAO.listar();
        for (int i = 0; i < Produtos.size(); i++) {
            jComboBox1.addItem(Produtos.get(i).getNome());
        }
            
    }

    public void carrega() throws ErpDAOException {
        ProdutoDAO MDAO = new ProdutoDAO();
        M = MDAO.procurar(id);
         
  
        jTextField4.setText(String.valueOf(M.getFornecedor_id()));
        jTextField6.setText(String.valueOf(M.getCategoria_id()));
        jTextField2.setText(M.getNome());
        jTextField3.setText(String.valueOf(M.getPreco()));
        jTextField5.setText(String.valueOf(M.getQuantidade_estoque()));
    }
