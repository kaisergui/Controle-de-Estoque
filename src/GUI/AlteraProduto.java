    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    Produto M = new Produto();
        M.setProduto_id(id);
        if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "O Campo nome não pode estar vazio");
        } else {

        try {
            M.setFornecedor_id(Integer.parseInt(jTextField4.getText()));
             M.setCategoria_id(Integer.parseInt(jTextField6.getText()));
            M.setNome(jTextField2.getText());
            M.setPreco(Float.parseFloat(jTextField3.getText()));
            M.setQuantidade_estoque(Integer.parseInt(jTextField5.getText()));
            ProdutoDAO PDAO = new ProdutoDAO();
            PDAO.atualizar(M);
            JOptionPane.showMessageDialog(rootPane, "Atualização efetuada com sucesso!!");
            dispose();
        } catch (ErpDAOException ex) {
            Logger.getLogger(AlteraProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

